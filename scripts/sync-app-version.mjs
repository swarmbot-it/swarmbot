#!/usr/bin/env node
/**
 * Reads the monorepo version from package.json and propagates it to:
 * - workspace package.json files (api, web)
 * - Angular build-time constant (build-version.ts)
 * Set SWARMBOT_VERSION in Docker/runtime to override the API runtime version.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const rootPkg = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));
const version = rootPkg.version;
if (!version || typeof version !== "string") {
	console.error("sync-app-version: missing version in root package.json");
	process.exit(1);
}

for (const rel of ["apps/api/package.json", "apps/web/package.json"]) {
	const path = join(root, rel);
	const pkg = JSON.parse(readFileSync(path, "utf8"));
	pkg.version = version;
	writeFileSync(path, `${JSON.stringify(pkg, null, 4)}\n`);
}

const out = join(root, "apps/web/src/app/core/build-version.ts");
writeFileSync(
	out,
	`/** Generated by scripts/sync-app-version.mjs — do not edit */\nexport const BUILD_APP_VERSION = "${version}";\n`
);

console.log(`sync-app-version: ${version}`);
